//Create a program that declares and initializes all primitive data types in Java and prints their default and assigned values.


public class Q01 {
    static int i;
    static float f;
    static double d;
    static char ch;
    static boolean b;
    static byte by;
    static short sh;
    static long l;
    static String S;
    
    public static void main(String[] args) {
        System.err.println("Default int: " +i);
        System.err.println("Default float: " +f);
        System.err.println("Default double: " +d);
        System.err.println("Default char: " +ch);
        System.err.println("Default boolean: " +b);
        System.err.println("Default byte: " +by);
        System.err.println("Default short: " +sh);
        System.err.println("Default long: " +l);
        System.err.println("Default String: " +S);
    

    }
}
