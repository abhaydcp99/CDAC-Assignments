// 5. Program to demonstrate binary, octal, hexadecimal, and floating-point literals
public class LiteralsDemo {
    public static void main(String[] args) {
        int binary = 0b1010; // Binary literal
        int octal = 012; // Octal literal
        int hex = 0xA; // Hexadecimal literal
        double floating = 10.5; // Floating-point literal
        
        System.out.println("Binary (0b1010): " + binary);
        System.out.println("Octal (012): " + octal);
        System.out.println("Hexadecimal (0xA): " + hex);
        System.out.println("Floating-point: " + floating);
    }
}