// 4. Program to calculate the average of three integers and display result as double
public class AverageCalculation {
    public static void main(String[] args) {
        int a = 10, b = 20, c = 30;
        double average = (a + b + c) / 3.0; // Typecasting ensures a double result
        System.out.println("Average: " + average);
    }
}