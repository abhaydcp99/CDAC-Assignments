public class ArithmeticOperations {
    public static void main(String[] args) {
        // Declare and initialize two integers
        int num1 = 10; // First number
        int num2 = 3;  // Second number

        // Performing arithmetic operations
        int sum = num1 + num2;        // Addition
        int difference = num1 - num2; // Subtraction
        int product = num1 * num2;    // Multiplication
        int quotient = num1 / num2;   // Division (integer division)
        int remainder = num1 % num2;  // Modulus (remainder)

        // Displaying results
        System.out.println("First Number: " + num1);
        System.out.println("Second Number: " + num2);
        System.out.println("Addition: " + sum);
        System.out.println("Subtraction: " + difference);
        System.out.println("Multiplication: " + product);
        System.out.println("Division: " + quotient);
        System.out.println("Modulus (Remainder): " + remainder);
    }
}


